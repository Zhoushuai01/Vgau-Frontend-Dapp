const analyzeLogoutFailure = (response) => {
  console.log('üîç ÂàÜÊûêÁôªÂá∫Â§±Ë¥•ÂéüÂõ†:', response)
  
  if (!response) {
    return 'RESPONSE_MISSING: Êé•Âè£ËøîÂõûÊï∞ÊçÆ‰∏∫Á©∫'
  }
  
  if (response.statusCode) {
    switch (response.statusCode) {
      case 401:
        return 'UNAUTHORIZED: Áî®Êà∑Êú™ÊéàÊùÉÊàñtokenÂ∑≤ËøáÊúü'
      case 403:
        return 'FORBIDDEN: ÊùÉÈôê‰∏çË∂≥'
      case 404:
        return 'NOT_FOUND: ÁôªÂá∫Êé•Âè£‰∏çÂ≠òÂú®'
      case 500:
        return 'SERVER_ERROR: ÊúçÂä°Âô®ÂÜÖÈÉ®ÈîôËØØ'
      case 502:
        return 'BAD_GATEWAY: ÁΩëÂÖ≥ÈîôËØØ'
      case 503:
        return 'SERVICE_UNAVAILABLE: ÊúçÂä°‰∏çÂèØÁî®'
      case 504:
        return 'GATEWAY_TIMEOUT: ÁΩëÂÖ≥Ë∂ÖÊó∂'
      default:
        return `HTTP_ERROR_${response.statusCode}: ${response.statusText || 'Êú™Áü•ÈîôËØØ'}`
    }
  }
  
  if (response.error) {
    return `API_ERROR: ${response.error}`
  }
  
  if (response.message) {
    return `MESSAGE: ${response.message}`
  }
  
  return 'UNKNOWN_ERROR: Êú™Áü•ÈîôËØØ'
}

