<script setup>
import { ref, computed, onMounted, watch } from 'vue'
import { useI18n } from 'vue-i18n'
import i18n from '@/i18n/i18n.js'
import contractExchange from '@/utils/contractExchange.js'

const { t } = useI18n()
// Ëé∑ÂèñÂä†ËΩΩÂºπÁ™óÁöÑÁ∫ØÊñáÂ≠óÊ†áÈ¢òÔºàÈÅøÂÖçÊòæÁ§∫ÈîÆÂÄºÂØπÔºâ
const getLoadingTitle = () => {
  try {
    const locale = i18n.global.locale?.value || 'en'
    return locale.startsWith('zh') ? 'Ê≠£Âú®ÂÖåÊèõ...' : 'Exchanging...'
  } catch (e) {
    return 'Exchanging...'
  }
}

// Ëé∑ÂèñÈîôËØØÊ†áÈ¢òÁöÑÁ∫ØÊñáÂ≠óÔºàÈÅøÂÖçÊòæÁ§∫ÈîÆÂÄºÂØπÔºâ
const getErrorTitle = () => {
  try {
    const locale = i18n.global.locale?.value || 'en'
    return locale.startsWith('zh') ? 'ÈåØË™§' : 'Error'
  } catch (e) {
    return 'Error'
  }
}

// Ëé∑ÂèñÁ°ÆËÆ§ÊåâÈíÆÊñáÂ≠óÁöÑÁ∫ØÊñáÂ≠óÔºàÈÅøÂÖçÊòæÁ§∫ÈîÆÂÄºÂØπÔºâ
const getConfirmText = () => {
  try {
    const locale = i18n.global.locale?.value || 'en'
    return locale.startsWith('zh') ? 'Á¢∫Ë™ç' : 'Confirm'
  } catch (e) {
    return 'Confirm'
  }
}

// Ëé∑ÂèñÈîôËØØ‰ø°ÊÅØÁöÑÁ∫ØÊñáÂ≠óÔºàÈÅøÂÖçÊòæÁ§∫ÈîÆÂÄºÂØπÔºâ
const getErrorMessage = (error) => {
  try {
    const locale = i18n.global.locale?.value || 'en'
    const isZh = locale.startsWith('zh')
    
    if (error.errorType === 'KYC_REQUIRED') {
      return error.message
    } else if (error.message) {
      if (error.message.includes('‰ΩôÈ¢ù‰∏çË∂≥') || error.message.includes('Insufficient balance')) {
        return error.message
      } else if (error.message.includes('Áî®Êà∑ÂèñÊ∂à') || error.message.includes('User rejected')) {
        return isZh ? 'Áî®Êà∂ÂèñÊ∂àÊìç‰Ωú' : 'User Cancelled Operation'
      } else if (error.message.includes('ÁΩëÁªú') || error.message.includes('network')) {
        return isZh ? 'Á∂≤Áµ°ÈåØË™§' : 'Network Error'
      } else if (error.message.includes('Gas') || error.message.includes('gas')) {
        return isZh ? 'GasË≤ªÁî®‰∏çË∂≥' : 'Insufficient Gas Fee'
      }
    }
    
    // ÈªòËÆ§ÈîôËØØ‰ø°ÊÅØ
    return isZh ? 'ÂÖåÊèõÂ§±Êïó' : 'Exchange Failed'
  } catch (e) {
    return 'Exchange Failed'
  }
}

// Ëé∑ÂèñÊàêÂäü‰ø°ÊÅØÁöÑÁ∫ØÊñáÂ≠óÔºàÈÅøÂÖçÊòæÁ§∫ÈîÆÂÄºÂØπÔºâ
const getSuccessMessage = () => {
  try {
    const locale = i18n.global.locale?.value || 'en'
    return locale.startsWith('zh') ? 'ÂÖåÊèõÊàêÂäü' : 'Exchange Successful'
  } catch (e) {
    return 'Exchange Successful'
  }
}

// Ëé∑ÂèñÊúâÊïàÈáëÈ¢ùÊèêÁ§∫ÁöÑÁ∫ØÊñáÂ≠óÔºàÈÅøÂÖçÊòæÁ§∫ÈîÆÂÄºÂØπÔºâ
const getValidAmountMessage = () => {
  try {
    const locale = i18n.global.locale?.value || 'en'
    return locale.startsWith('zh') ? 'Ë´ãËº∏ÂÖ•ÊúâÊïàÈáëÈ°ç' : 'Please Enter Valid Amount'
  } catch (e) {
    return 'Please Enter Valid Amount'
  }
}

// ÂÖëÊç¢Êï∞Èáè
const exchangeAmount = ref('')
const isLoading = ref(false)
const currentPrice = ref(120) // ÈªòËÆ§‰ª∑Ê†ºÔºåÂ∞Ü‰ªéÂêàÁ∫¶Ëé∑Âèñ

// Ëá™ÂÆö‰πâÈîôËØØÂºπÁ™óÁõ∏ÂÖ≥Áä∂ÊÄÅ
const showErrorModal = ref(false)
const errorModalData = ref({
  title: '',
  message: '',
  confirmText: ''
})

// ËÆ°ÁÆóÊâÄÈúÄUSDT
const requiredUSDT = ref('0')

// ËÆ°ÁÆóÊâÄÈúÄUSDTÁöÑÂáΩÊï∞
const calculateRequiredUSDT = async () => {
  if (!exchangeAmount.value || isNaN(exchangeAmount.value)) {
    requiredUSDT.value = '0'
    return
  }
  
  try {
    const amount = parseFloat(exchangeAmount.value)
    console.log('üîç ËÆ°ÁÆóÊâÄÈúÄUSDT:', amount)
    
    const result = await contractExchange.getRequiredUSDT(amount)
    
    console.log('üìä ËÆ°ÁÆóÁªìÊûú:', result)
    // Áõ¥Êé•‰ΩøÁî®Êï∞Â≠óÂÄºÔºå‰øùÁïô‰∏§‰ΩçÂ∞èÊï∞
    requiredUSDT.value = result.formatted.toFixed(2)
  } catch (error) {
    console.error('ËÆ°ÁÆóÊâÄÈúÄUSDTÂ§±Ë¥•:', error)
    requiredUSDT.value = '0'
  }
}

// Ëé∑ÂèñÊúÄÊñ∞‰ª∑Ê†º
const getLatestPrice = async () => {
  try {
    const price = await contractExchange.getLatestGoldPrice()
    // ÈªÑÈáë‰ª∑Ê†ºÈÄöÂ∏∏Êúâ8‰ΩçÂ∞èÊï∞
    currentPrice.value = parseFloat(price) / Math.pow(10, 8)
    console.log('Ëé∑ÂèñÂà∞ÊúÄÊñ∞ÈªÑÈáë‰ª∑Ê†º:', currentPrice.value)
  } catch (error) {
    console.error('Ëé∑Âèñ‰ª∑Ê†ºÂ§±Ë¥•:', error)
    currentPrice.value = 120
  }
}

// Á°ÆËÆ§ÂÖëÊç¢
const confirmExchange = async () => {
  if (!exchangeAmount.value || parseFloat(exchangeAmount.value) <= 0) {
    uni.showToast({
      title: getValidAmountMessage(),
      icon: 'none',
      duration: 2000
    })
    return
  }

  if (isLoading.value) {
    return
  }

  try {
    isLoading.value = true
    
    // ÊòæÁ§∫Âä†ËΩΩÊèêÁ§∫Ôºà‰ΩøÁî®Á∫ØÊñáÂ≠óÔºåÈÅøÂÖçÈîÆÂÄºÂØπÊòæÁ§∫Ôºâ
    uni.showLoading({
      title: getLoadingTitle(),
      mask: true
    })

    console.log('üöÄ ÂºÄÂßãUSDTÂÖëÊç¢VGAUÊµÅÁ®ã...')
    console.log('ÂÖëÊç¢Êï∞Èáè:', exchangeAmount.value, 'VGAU')

    // Ë∞ÉÁî®ÂêàÁ∫¶ÂÖëÊç¢
    const result = await contractExchange.exchangeUsdtToVgau(exchangeAmount.value)
    
    // ÈöêËóèÂä†ËΩΩÊèêÁ§∫
    uni.hideLoading()

    console.log('‚úÖ ÂÖëÊç¢ÊàêÂäü:', result)

    // ÊòæÁ§∫ÊàêÂäüÊèêÁ§∫
    uni.showToast({
      title: getSuccessMessage(),
      icon: 'success',
      duration: 3000
    })

    // Ê∏ÖÁ©∫ËæìÂÖ•Ê°Ü
    exchangeAmount.value = ''

    // ÂèØ‰ª•Âú®ËøôÈáåÊ∑ªÂä†Ë∑≥ËΩ¨Âà∞ÊàêÂäüÈ°µÈù¢ÊàñÂÖ∂‰ªñÈÄªËæë
    setTimeout(() => {
      uni.navigateBack()
    }, 2000)

  } catch (error) {
    console.error('‚ùå ÂÖëÊç¢Â§±Ë¥•:', error)
    
    // ÈöêËóèÂä†ËΩΩÊèêÁ§∫
    uni.hideLoading()
    
    // ÊòæÁ§∫ÈîôËØØ‰ø°ÊÅØ - ‰ΩøÁî®Ëá™ÂÆö‰πâÂºπÁ™óÈÅøÂÖçÈîÆÂÄºÂØπÊòæÁ§∫
    errorModalData.value = {
      title: getErrorTitle(),
      message: getErrorMessage(error),
      confirmText: getConfirmText()
    }
    showErrorModal.value = true
  } finally {
    isLoading.value = false
  }
}

// ËøîÂõû‰∏ä‰∏ÄÈ°µ
const goBack = () => {
  uni.navigateBack()
}

// ÂÖ≥Èó≠ÈîôËØØÂºπÁ™ó
const closeErrorModal = () => {
  showErrorModal.value = false
}

// ÁõëÂê¨ÂÖëÊç¢Êï∞ÈáèÂèòÂåñÔºåËá™Âä®ËÆ°ÁÆóÊâÄÈúÄUSDT
watch(exchangeAmount, () => {
  calculateRequiredUSDT()
}, { immediate: false })

// È°µÈù¢Âä†ËΩΩÊó∂Ëé∑ÂèñÊúÄÊñ∞‰ª∑Ê†º
onMounted(async () => {
  try {
    await getLatestPrice()
  } catch (error) {
    console.error('ÂàùÂßãÂåñ‰ª∑Ê†ºÂ§±Ë¥•:', error)
  }
})
</script>

